//add two numbers
function addTwoNumber(a,b){
    let sum=a+b;
    return sum;
}
let total= addTwoNumber(50,50);
console.log("Addition is :",total);

// multiply two numbers
function multiplyTwoNumbers(c,d){
    let multiply=c*d;
    return multiply;
}
let multiplication= multiplyTwoNumbers(5,5);
console.log("Multiplication is :",multiplication);

//division two numbers
function divisionTwoNumbers(x,y){
    let division=x/y;
    return division;
}
let quotient= divisionTwoNumbers(50,5);
console.log("Quotient is :",quotient);

// subtraction two numbers
function subtractionTwoNumbers(m,n){
    let subtraction=m-n;
    return subtraction;
}
let sub=subtractionTwoNumbers(30,10)
console.log("Subtraction is :",sub);
